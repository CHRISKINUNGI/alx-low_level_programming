#include "3-calc.h"

/**
 * op_add - function to perform addition
 *
 * @a:first digit
 * @b:second digit
 *
 * Return: sum
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to perform subtraction
 *
 * @a:first digit
 * @b:second digit
 *
 * Return: difference
 *
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to perform multiplication
 *
 * @a:first digit
 * @b:second digit
 *
 * Return: product
 *
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to perform division
 *
 * @a:first digit
 * @b:second digit
 *
 * Return: quotient
 *
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- function to perform modulous
 *
 * @a:first digit
 * @b:second digit
 *
 * Return: remeinder
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}
